CREATE TABLE CF_LOV_DOMAINS 
(
  ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY INCREMENT BY 1 MAXVALUE 9999999999 MINVALUE 1 NOCACHE NOT NULL 
, DOMAIN_NAME VARCHAR2(255 CHAR) NOT NULL 
, CODE VARCHAR2(30 CHAR) NOT NULL 
, DESCRIPTION VARCHAR2(4000 CHAR) 
, CREATED_BY VARCHAR2(100 CHAR) NOT NULL 
, CREATED_ON DATE NOT NULL 
, MODIFIED_BY VARCHAR2(100 CHAR) 
, MODIFIED_ON DATE 
, CONSTRAINT CF_LOV_DOMAINS_PK PRIMARY KEY (ID)
, CONSTRAINT CF_LOV_DOMAIN_CODE_ID_UN UNIQUE (CODE)
);
