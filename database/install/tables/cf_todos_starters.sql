CREATE TABLE CF_TODOS_STARTERS 
(
  ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY INCREMENT BY 1 MAXVALUE 9999999999 MINVALUE 1 NOCACHE NOT NULL 
, TODO_ID NUMBER NOT NULL 
, STARTER_ID NUMBER NOT NULL 
, CHECKED NUMBER(1, 0) DEFAULT 0 
, COMPLETED_BY VARCHAR2(100 CHAR) 
, COMPLETED_ON DATE 
, CREATED_BY VARCHAR2(100 CHAR) NOT NULL 
, CREATED_ON DATE NOT NULL 
, MODIFIED_BY VARCHAR2(100 CHAR) 
, MODIFIED_ON DATE 
, CONSTRAINT CF_TODOS_STARTERS_PK PRIMARY KEY (ID)
, CONSTRAINT CF_TODOS_STARTERS_STARTERS_FK FOREIGN KEY (STARTER_ID) REFERENCES CF_STARTERS (ID)
, CONSTRAINT CF_TODOS_STARTERS_TODOS_FK FOREIGN KEY (TODO_ID) REFERENCES CF_TODOS (ID)
);