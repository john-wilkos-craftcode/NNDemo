CREATE TABLE CF_STARTERS_EMPLOYEES 
(
  ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY INCREMENT BY 1 MAXVALUE 9999999999 MINVALUE 1 NOCACHE NOT NULL 
, STARTER_ID NUMBER 
, EMPLOYEE_EMAIL VARCHAR2(100 CHAR)
, CREATED_BY VARCHAR2(100 CHAR) NOT NULL 
, CREATED_ON DATE NOT NULL 
, MODIFIED_BY VARCHAR2(100 CHAR) 
, MODIFIED_ON DATE 
, CONSTRAINT CF_STARTERS_EMPLOYEES_PK PRIMARY KEY (ID) 
, CONSTRAINT CF_STARTERS_EMPLOYEES_FK FOREIGN KEY (STARTER_ID) REFERENCES CF_STARTERS (ID)
);