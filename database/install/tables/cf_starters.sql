CREATE TABLE CF_STARTERS 
(
  ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY INCREMENT BY 1 MAXVALUE 9999999999 MINVALUE 1 NOCACHE NOT NULL 
, TOKEN VARCHAR2(50 CHAR) NOT NULL 
, FIRST_NAME VARCHAR2(50 CHAR) NOT NULL 
, LAST_NAME VARCHAR2(50 CHAR) NOT NULL 
, COMPANY_ID NUMBER NOT NULL 
, POSITION VARCHAR2(50 CHAR) 
, LANGUAGE VARCHAR2(5 CHAR) 
, START_DATE DATE 
, IS_ACTIVE NUMBER(1, 0) DEFAULT 1 
, ADDRESS_LINE_1 VARCHAR2(200 CHAR) 
, ADDRESS_LINE_2 VARCHAR2(200 CHAR) 
, CITY VARCHAR2(50 CHAR) 
, ZIPCODE NUMBER(4, 0) 
, COUNTRY VARCHAR2(50 CHAR) 
, PERSONAL_EMAIL VARCHAR2(50 CHAR) 
, BUSINESS_EMAIL VARCHAR2(50 CHAR) 
, TELEPHONE VARCHAR2(20 CHAR) 
, CHOICE_LAPTOP VARCHAR2(50 CHAR) 
, CHOICE_PHONE VARCHAR2(50 CHAR) 
, PROFILE_PICTURE BLOB 
, MIMETYPE VARCHAR2(50 CHAR) 
, FILENAME VARCHAR2(200 CHAR) 
, CITY_ID NUMBER 
, CREATED_DATE_PIC DATE 
, AGENDA_STARTER CLOB 
, INTRODUCTION VARCHAR2(1000 CHAR) 
, PREVIOUS_EXPERIENCE VARCHAR2(1000 CHAR) 
, FUNCTION_AND_COMPANY VARCHAR2(1000 CHAR) 
, CHILDREN NUMBER 
, PETS VARCHAR2(100 CHAR) 
, HOBBIES VARCHAR2(100 CHAR) 
, FORM_PICTURE BLOB 
, FORM_FILENAME VARCHAR2(100 CHAR) 
, FORM_MIMETYPE VARCHAR2(100 CHAR) 
, WELCOME_TEXT_COORDINATOR CLOB 
, AGENDA_CHECKED NUMBER(1, 0) 
, ENDPOINT VARCHAR2(250 CHAR) 
, P256DH VARCHAR2(250 CHAR) 
, AUTH VARCHAR2(250 CHAR) 
, CREATED_BY VARCHAR2(100 CHAR) NOT NULL 
, CREATED_ON DATE NOT NULL 
, MODIFIED_BY VARCHAR2(100 CHAR) 
, MODIFIED_ON DATE 
, CONSTRAINT CF_STARTERS_PK PRIMARY KEY (ID)
, CONSTRAINT CF_STARTERS_TOKEN_UN UNIQUE (TOKEN)
, CONSTRAINT CF_STARTERS_CF_COMPANY_FK FOREIGN KEY (COMPANY_ID) REFERENCES CF_COMPANIES (ID)
, CONSTRAINT CF_STARTERS_CITIES_FK FOREIGN KEY (CITY_ID) REFERENCES CF_CITIES (ID)
);