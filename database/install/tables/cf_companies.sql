CREATE TABLE CF_COMPANIES 
(
  ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY INCREMENT BY 1 MAXVALUE 9999999999 MINVALUE 1 NOCACHE NOT NULL 
, NAME VARCHAR2(50 CHAR) NOT NULL 
, ABOUT VARCHAR2(1000 CHAR) 
, HEX_CODE VARCHAR2(7 BYTE) 
, STARTDAY_MORNING CLOB 
, STARTDAY_EVENING CLOB 
, WELCOMETEXT CLOB 
, FACEBOOK VARCHAR2(100 CHAR) 
, TWITTER VARCHAR2(100 CHAR) 
, LINKEDIN VARCHAR2(100 CHAR) 
, INSTAGRAM VARCHAR2(100 CHAR) 
, LOGO BLOB 
, MIMETYPE VARCHAR2(50 BYTE) 
, FILENAME VARCHAR2(200 BYTE) 
, UPDATED_LOGO DATE 
, YOUTUBE VARCHAR2(100 CHAR) 
, CREATED_BY VARCHAR2(100 CHAR) NOT NULL 
, CREATED_ON DATE NOT NULL 
, MODIFIED_BY VARCHAR2(100 CHAR) 
, MODIFIED_ON DATE 
, CONSTRAINT CF_COMPANY_PK PRIMARY KEY (ID)
);

