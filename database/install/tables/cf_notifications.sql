CREATE TABLE CF_NOTIFICATIONS 
(
  ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY INCREMENT BY 1 MAXVALUE 9999999999 MINVALUE 1 NOCACHE NOT NULL 
, STARTER_ID NUMBER 
, TITLE VARCHAR2(50 CHAR) 
, TEXT_MESSAGE VARCHAR2(200 CHAR) 
, NOTIFICATION_TIME DATE 
, CREATED_BY VARCHAR2(100 CHAR) NOT NULL 
, CREATED_ON DATE NOT NULL 
, MODIFIED_BY VARCHAR2(100 CHAR) 
, MODIFIED_ON DATE 
, CONSTRAINT CF_NOTIFICATIONS_PK PRIMARY KEY (ID)
, CONSTRAINT CF_NOTIFICATIONS_STARTERS_FK FOREIGN KEY (STARTER_ID) REFERENCES CF_STARTERS (ID)
);