CREATE TABLE CF_STARTERS_NOTIFICATIONS_PWA 
(
  ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY INCREMENT BY 1 MAXVALUE 9999999999 MINVALUE 1 NOCACHE NOT NULL 
, STARTER_ID NUMBER 
, NOTIFICATION_ID NUMBER 
, TIME_RECEIVED DATE 
, SEEN NUMBER(1, 0) DEFAULT 0 
, CREATED_BY VARCHAR2(100 CHAR) NOT NULL 
, CREATED_ON DATE NOT NULL 
, MODIFIED_BY VARCHAR2(100 CHAR) 
, MODIFIED_ON DATE 
, CONSTRAINT PK_STARTERS_NOTIFICATIONS_PWA PRIMARY KEY (ID) 
, CONSTRAINT FK_PWA_NOTIFICATIONS_NOTIFICATIONS FOREIGN KEY (NOTIFICATION_ID) REFERENCES CF_NOTIFICATIONS_PWA (ID)
, CONSTRAINT FK_PWA_NOTIFICATIONS_STARTERS FOREIGN KEY (STARTER_ID) REFERENCES CF_STARTERS (ID)
);